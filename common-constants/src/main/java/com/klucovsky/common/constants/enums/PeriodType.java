/**
 * Enum of period type
 */
public enum PeriodType {

    /**
     * Period of current year
     */
    CURRENT_YEAR,

    /**
     * Period of last year
     */
    LAST_YEAR,

    /**
     * Period of 180 days
     */
    LAST_180_DAYS,

    /**
     * Period of current quarter
     */
    CURRENT_QUARTER,

    /**
     * Period of last quarter
     */
    LAST_QUARTER,

    /**
     * Period of last 90 days
     */
    LAST_90_DAYS,

    /**
     * Period of current month
     */
    CURRENT_MONTH,

    /**
     * Period of last month
     */
    LAST_MONTH,

    /**
     * Period of last 30 days
     */
    LAST_30_DAYS,

    /**
     * Period of current week
     */
    CURRENT_WEEK,

    /**
     * Period of last week
     */
    LAST_WEEK,

    /**
     * Period of current today
     */
    TODAY,

    /**
     * Period of yesterday
     */
    YESTERDAY
}

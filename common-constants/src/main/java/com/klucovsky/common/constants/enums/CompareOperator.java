package com.klucovsky.common.constants.enums;

/**
 * Enum of compare operator
 */
public enum CompareOperator {

    /**
     * Equal
     */
    EQ,

    /**
     * Not equal
     */
    NE,

    /**
     * Lower than
     */
    LT,

    /**
     * Lower or equal
     */
    LE,

    /**
     * Grater than
     */
    GT,

    /**
     * Greater or equal
     */
    GE
}

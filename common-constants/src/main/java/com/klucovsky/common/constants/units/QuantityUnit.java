package com.klucovsky.common.constants.units;

/**
 * Units to define quantity
 */
public enum QuantityUnit {

    /**
     * Quantity expressed by number of pieces
     */
    PIECE,

    /**
     * Quantity expressed by length
     */
    LENGTH,

    /**
     * Quantity expressed by weight
     */
    MASS,

    /**
     * Quantity expressed by volume
     */
    VOLUME,

    /**
     * Quantity expressed by area
     */
    AREA;
}
